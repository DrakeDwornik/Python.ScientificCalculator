import math

class Calculator:
    def __init__(self):
        pass

    def add(x, y):
        print(x + y)
        return x + y

    def subtract(x, y):
        print(x - y)
        return x - y

    def multiply(x, y):
        print(x * y)
        return y * x

    def divide(x, y):
        print(x / y)
        return x / y


    def square(base):
        print(base ** 2)
        return base ** 2


    def exp(x, y):
        print(x ** y)
        return x ** y


    def square_root(x):
        print(x ** (1 / 2))
        return x ** (1 / 2)


    def inv(x):
        print(1 / x)
        return 1 / x